            <h2>Debt Resolution Legal Service</h2>
            <p>If you owe over $10,000 in unsecured debt and are struggling with payments, our Debt Resolution Legal Service may be your best course of action to resolve your debt. Our Attorneys and Professional staff will assist you in resolving your debts on a plan that will fit your personal situation and budget.</p>
            <p>Once you are a client of National Legal Center, using our Debt Resolution Legal Service, you will be set up with an affordable monthly payment based on how much debt you owe, your budget and your personal goals.  Each month, your payment will be drafted from your personal bank account and be deposited in your Dedicated Account where all of your funds will be held under your name and social security for the purpose of settlement savings and disbursements. A portion of your payment is allocated towards the Firmâ€™s fee and the rest is allocated toward your savings for your settlements. Your savings will accumulate each month, so that in time, lump sum funds become available to offer settlements to your creditors.</p>
            <p>Each client's situation is different and the negotiation process will begin at different times depending on the amount of your debt, the particular creditors you owe and when funds become available in your resolution account. Once a favorable settlement is reached that you approve, we will instruct the creditor to send us the settlement offer in writing. Once an account is settled, the cycle continues as we move through each debt, until all of your debts have been settled.</p>
