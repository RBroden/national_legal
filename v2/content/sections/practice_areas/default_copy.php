            <h2>FDCPA Enforcement</h2>
            <p>The Fair Debt Collection Practices Act, which is often referred to as the FDCPA, was created by Congress in 1977 in response to the abusive conduct often engaged in by collection agencies.  The FDCPA is in place to protect you, the consumer, against the illegal tactics of the third party creditors.</p>
            <p>Our firm has a strong understanding of and experience with the debt collection laws under the FDCPA. If  you feel you have experienced a third party debt collector that has crossed the line by saying something or  doing something that makes you feel you are in violation of your civil rights, National Legal Center may  be able to help.</p>
            <ul>
               <li>Has a collector used profanities when speaking to you?</li>
               <li>Has a collector verbally abused you or threatened to physically harm you?</li>
               <li>Has a collector disclosed personal, confidential information to a family member, friend or co-worker without your permissions?</li>
               <li>Has a collector ever threatened to put you in jail?</li>
               <li>Is a collector calling you at work, even though you have asked them to stop?</li>
            </ul>
            <p>These are all common violations of the FDCPA. If they you are experiencing situations like the ones outlined above, contact our office for a free consultation. We will always be honest and fair with you. Our representation may include educating you as to your rights under the Act and/or taking legal action against a debt collector that violates your rights under the FDCPA.</p>
            <p><a href="http://business.ftc.gov/documents/fair-debt-collection-practices-act">View the complete Fair Debt  Collection Practices Act.</a></p>
