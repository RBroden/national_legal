            <h2>FDCPA Enforcement</h2>
            <p>Our Attorneys have extensive experience with the debt collection laws under The Fair Debt Collection Practices Act (FDCPA). We pride ourselves on aggressively defending our client’s rights under these laws.</p>
            <p>The FDCPA is a federal regulation designed to protect consumers against the highly aggressive tactics often used by debt collectors. Under this act, consumers have the power to fight back against creditor harassment.</p>
            <p>If you feel that you may be a victim of an FDCPA violation, National Legal Center may be able to help.</p>
            <p>Some common violations include:</p>
            <ul>
               <li>A collector using profanities when speaking to you.</li>
               <li>A collector verbally abusing you or threatening to physically harm you.</li>
               <li>A collector disclosing personal, confidential information to a family member, friend or co-worker without your permission.</li>
               <li>A collector threatening to incarcerate you.</li>
               <li>A collector calling you at work, even though you have asked them to stop.</li>
               <li>A collector attempting to collect on an invalid debt, including debt not belonging to you.</li>
            </ul>
            <p>If you are experiencing situations similar to these, contact our office for a free consultation. We will always be honest and fair with you. Our representation may include further educating you as to your rights under the Act and/or taking legal action against a debt collector that violates your rights under the FDCPA.</p>
